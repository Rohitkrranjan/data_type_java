public class Variables {
    public static void main(String[] args)
    {
      // integer data type
        int myNumber=10;
        System.out.println(myNumber);

        // float data type
        float yourNumber = 12;
        System.out.println(yourNumber);
        // double data type
        double x = 12.34;
        System.out.println(x);

        // boolean data type
        boolean y=true;
        System.out.println((y));

        // string
        String name = "Rohit";
        System.out.println(name);
    }
}
