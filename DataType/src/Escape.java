public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello \"Rohit\"...");
        System.out.println("Hello \'Rohit\'...");
        System.out.println("Hello \\Rohit\\...");
        System.out.println("Hello \nRohit...");
        System.out.println("Hello \tRohit...");
        System.out.println("Hello\bRohit...");
    }
}
